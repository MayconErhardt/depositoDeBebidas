    private void botAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botAlterarActionPerformed

        int linha = -1;
        linha = tabFuncionario.getSelectedRow();
        if (linha != -1) {
            boolean consulta = false;
            cliente = listCliente.get(linha);
            viewDadosCliente alterar = new viewDadosCliente(null, true, cliente, consulta);
            alterar.setVisible(true);
            listCliente.clear();
            listCliente.addAll(contCliente.listarTodos());
            limparLab();
            cliente = null;
        } else {
            JOptionPane.showMessageDialog(null, "SELECIONE ALGUÉM NA TABELA PARA ALTERAR");
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_botAlterarActionPerformed

    private void botExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botExcluirActionPerformed

        int linhaExcluir = -1;
        linhaExcluir = tabFuncionario.getSelectedRow();
        if (linhaExcluir != -1) {
            cliente = listCliente.get(linhaExcluir);
            int op = JOptionPane.showConfirmDialog(this,
                    "Confirma a exclusão do Cliente "
                    + cliente.getClienteNome() + "?",
                    "Exclusão de Cliente",
                    JOptionPane.YES_NO_OPTION);

            if (op == JOptionPane.YES_OPTION) {
                try {
                    contCliente.excluir(cliente.getClienteId());
                    listCliente.clear();
                    listCliente.addAll(contCliente.listarTodos());
                    limparLab();
                } catch (NaoExisteException | IntegridadeException e){
                    
                    JOptionPane.showMessageDialog(this, e.getMessage());
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Cliente Na Tabela Para Ser Excluido");
        }

    }//GEN-LAST:event_botExcluirActionPerformed


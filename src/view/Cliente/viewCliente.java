    private void tabFuncionarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabFuncionarioMouseClicked
        int linha = -1;
        linha = tabFuncionario.getSelectedRow();

        if (evt.getClickCount() > 1) {
            boolean consulta = true;
            viewDadosCliente insAlte = new viewDadosCliente(null, true, cliente, consulta);
            insAlte.setVisible(true);
            limparLab();
            listCliente.clear();
            listCliente.addAll(contCliente.listarTodos());
        } else {
            if (linha != -1) {
                cliente = new Cliente();
                cliente = listCliente.get(linha);
                labelNome.setText(cliente.getClienteNome());

                labCidades.setText(cliente.getClienteCidade() + ", Estado: " + cliente.getClienteEstado());
                //   labCidades.setText(Cliente.getClienteRua()+", Estado: "+ Cliente.getClienteEstado());
                laBNomeRua.setText(cliente.getClienteRua() + ", Nº " + String.valueOf(cliente.getClienteRuaNumero()));

                //   laBNomeRua.setText(Cliente.getClienteCidade()+", Nº "+ String.valueOf(Cliente.getClienteRuaNumero()));
                labRG.setText(cliente.getClienteRG());
                labBairro.setText(cliente.getClienteBairro());
                labCPF.setText(cliente.getClienteCPF());
                labDataNasc.setText(formatar.format(cliente.getClienteDataNascimento()));
                labEmail.setText(cliente.getClienteEmail());
//                labEstado.setText(Cliente.getClienteEstado());
//                labNumRua.setText(String.valueOf(Cliente.getClienteRuaNumero()));
                labTelefone.setText(cliente.getClienteTelefoneCelular());
                //  JOptionPane.showMessageDialog(null, Cliente);
            }
        }
    }//GEN-LAST:event_tabFuncionarioMouseClicked


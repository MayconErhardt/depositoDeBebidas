    public String doubleString(double d) {

        Locale.setDefault(new Locale("pt", "BR"));
        DecimalFormat dinheiro = new DecimalFormat("#,##0.00", new DecimalFormatSymbols(new Locale("pt", "BR")));
        String s = dinheiro.format(d);
        return s;
    }

    public int dias() {
        int dias;
        LocalDateTime from = LocalDateTime.ofInstant(pag.getDataVencimento().toInstant(), ZoneId.systemDefault());
        LocalDateTime to = LocalDateTime.ofInstant(hoje.toInstant(), ZoneId.systemDefault());
        Duration d = Duration.between(from, to);
        dias = (int) d.toDays();
        // JOptionPane.showMessageDialog(null, dias);
        return dias;
    }


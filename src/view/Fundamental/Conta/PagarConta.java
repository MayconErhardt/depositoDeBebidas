    private void botConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botConfirmarActionPerformed
        if (ControleCaixa.isCaixaAberto() != null) {

            if (!(labValorAPagar.getText().equals(""))) {

                try {
                    pag.setDataVencimento(df.parse(labVencimento.getText()));

                    pag.setDataPaga(df.parse(labDataPagamento.getText()));
                    double valorDesconto = ((Number) formaDesconto.getValue()).doubleValue();
                    pag.setDesconto((valorDesconto));
                    //pag.setObservacao("Foi dados um desconto de " + pag.getDesconto());
                    double valorJuro = ((Number) formaJuros.getValue()).doubleValue();
                    pag.setJuros((valorJuro));

                    if (valorDesconto == 0.00 && valorJuro != 0.00) {
                        pag.setObservacao("Foi cobrado um juros de " + pag.getJuros());
                    }
                    if (valorDesconto != 0.00 && valorJuro == 0.00) {
                        pag.setObservacao("Foi cobrado um desconto de " + pag.getDesconto());
                    }

                    if (valorDesconto == 0.00 && valorJuro == 0.00) {
                        pag.setObservacao("Sem observações");
                    }
                    if (valorDesconto != 0.00 && valorJuro != 0.00) {
                        pag.setObservacao("Foi dado um desconto de " + pag.getDesconto()+"."+"   Foi cobrado um juros de " + pag.getJuros());
                    }
                    
                    
                    String valor = labValorAPagar.getText();
                    String comVirgula = valor.replace(',', '.');
                    pag.setValorPagar(Double.parseDouble(comVirgula));
                    pag.setCaixa(ControleCaixa.getCaixa());
                    ControleCaixa.getCaixa().getListPagamento().add(pag);
                    contPag.pagar(pag);
                    dispose();
                } catch (ParseException ex) {
                    Logger.getLogger(PagarConta.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(null, "SELECIONE O VALOR PAGO DE PAGAMENTO!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "O CAIXA TEM QUE ESTAR ABERTO PARA PODER TER UM PAGAMENTO");
        }
    }//GEN-LAST:event_botConfirmarActionPerformed


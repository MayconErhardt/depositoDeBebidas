    private void botDevolverProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botDevolverProdutoActionPerformed

        int linhaTabProdutoOrcamento = -1;

        linhaTabProdutoOrcamento = tabProdutoOrcamento.getSelectedRow();

        if (!(listOrcamentoProduto.isEmpty())) {
            if (linhaTabProdutoOrcamento != -1) {

                if (jSpinerQuantidadeDevolvida.getValue() > -1) {
                    orcProdutoDevolver = listOrcamentoProduto.get(linhaTabProdutoOrcamento);

                    orcProdutoDevolver.setQuantidadeDevolvida(jSpinerQuantidadeDevolvida.getValue());

                    listOrcamentoProdutoDevolvido.add(orcProdutoDevolver);

                    atualizarTabelaProdutoOrcamentoDevolvido(listOrcamentoProdutoDevolvido);

                    listOrcamentoProduto.remove(linhaTabProdutoOrcamento);

                    atualizarTabelaProdutoOrcamento(listOrcamentoProduto);

                    labValorAPagar.setText(calcularValorDeProdutosDevolvidos(listOrcamentoProdutoDevolvido));
                    jSpinerQuantidadeDevolvida.setValue(0);
                } else {
                    JOptionPane.showMessageDialog(null, "QUANTIDADE DEVOLVIDA INVÁLIDO!");
                }

            } else {
                JOptionPane.showMessageDialog(null, "SELECIONE UM PRODUTO PARA SER DEVOLVIDO!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "NÃO TEM PRODUTOS PARA SER DEVOLVIDOS!");
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_botDevolverProdutoActionPerformed

    private void botVoltarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botVoltarProdutoActionPerformed

        if (!(listOrcamentoProdutoDevolvido.isEmpty())) {

            int linhaVoltarDevolucao = -1;
            linhaVoltarDevolucao = tabProdutosDevolvidos.getSelectedRow();

            if (linhaVoltarDevolucao != -1) {
                OrcamentoProduto orcProdDevol = new OrcamentoProduto();
                orcProdDevol = listOrcamentoProdutoDevolvido.get(linhaVoltarDevolucao);
                orcProdDevol.setQuantidadeDevolvida(0);
                listOrcamentoProduto.add(orcProdDevol);
                listOrcamentoProdutoDevolvido.remove(linhaVoltarDevolucao);
                atualizarTabelaProdutoOrcamento(listOrcamentoProduto);
                atualizarTabelaProdutoOrcamentoDevolvido(listOrcamentoProdutoDevolvido);
                labValorAPagar.setText(calcularValorDeProdutosDevolvidos(listOrcamentoProdutoDevolvido));
                orcamentoDevolvido.setListaOrcamento(listOrcamentoProdutoDevolvido);
            } else {
                JOptionPane.showMessageDialog(null, "SELECIONE UM PRODUTO PARA SER CORRIGIDO SUA DEVOLUÇÃO!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "NÃO TEM PRODUTOS DEVOLVIDOS!");
        }

    }//GEN-LAST:event_botVoltarProdutoActionPerformed


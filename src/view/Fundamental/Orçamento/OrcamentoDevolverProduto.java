    private void atualizarTabelaProdutoOrcamento(ArrayList<OrcamentoProduto> listaProdutoOrcamento) {

        while (tabProdutoADevolver.getRowCount() > 0) {
            // apaga a primeira linha
            tabProdutoADevolver.removeRow(0);
        }
        for (OrcamentoProduto o : listaProdutoOrcamento) {
            // adiciona uma linha na tabela
            // o object criado tem que ter a mesma quantidade
            // de elementos que foi definida na tabela, 
            // como também os mesmos tipos
            tabProdutoADevolver.addRow(new Object[]{o.getProdutoCod().getProdutoNome(), doubleString(o.getValor()), o.getQuantidade()});
        }
    }

    public String doubleString(double d) {

        Locale.setDefault(new Locale("pt", "BR"));
        DecimalFormat df = new DecimalFormat("#,##0.00", new DecimalFormatSymbols(new Locale("pt", "BR")));
        String s = df.format(d);
        return s;
    }

    private void atualizarTabelaProdutoOrcamentoDevolvido(ArrayList<OrcamentoProduto> listaProdutoOrcamentoDevolvidos) {

        while (tabProdutoDevolvido.getRowCount() > 0) {
            // apaga a primeira linha
            tabProdutoDevolvido.removeRow(0);
        }
        for (OrcamentoProduto o : listaProdutoOrcamentoDevolvidos) {
            // adiciona uma linha na tabela
            // o object criado tem que ter a mesma quantidade
            // de elementos que foi definida na tabela, 
            // como também os mesmos tipos
            tabProdutoDevolvido.addRow(new Object[]{o.getProdutoCod().getProdutoNome(), doubleString(o.getValor()), o.getQuantidadeDevolvida()});
        }
    }

    private String calcularValorDeProdutosDevolvidos(ArrayList<OrcamentoProduto> listaProdDevolvido) {
        double valor = 0;

        for (OrcamentoProduto op : listaProdDevolvido) {
            valor += op.getValor() * (op.getQuantidade() - op.getQuantidadeDevolvida());
        }
        return doubleString(valor);
    }

    

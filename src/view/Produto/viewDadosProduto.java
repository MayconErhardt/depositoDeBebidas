    private void editarTxt() {
        txtNomeProduto.setBorder(BorderFactory.createMatteBorder(0, 0, 2, 0, Color.BLACK));
        
        txtValorVenda.setBorder(BorderFactory.createMatteBorder(0, 0, 2, 0, Color.BLACK));
    }

    private void setarProduto() {
        txtNomeProduto.setText(produto.getProdutoNome());

        Float valor = (produto.getProdutoValorVenda());
        DecimalFormat df = new DecimalFormat("0.00");

        String v = String.valueOf(df.format(valor));
        jSpinQuantMinima.setValue(produto.getQuantidadeMinima());
        String comVirgula = v.replace('.', ',');
        txtValorVenda.setText(comVirgula);
        labValorUltimaCompra.setText(doubleString(produto.getProdutoValorCompra()));       
    }
    
    
    public String doubleString(double d) {
        
        Locale.setDefault(new Locale("pt", "BR"));
        DecimalFormat df = new DecimalFormat("#,##0.00", new DecimalFormatSymbols(new Locale("pt", "BR")));
        String s = df.format(d);
        return s;
    }

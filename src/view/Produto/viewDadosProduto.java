    private void botSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botSalvarActionPerformed

        contProd = new ControleProduto();
        int quantidade = jSpinQuantMinima.getValue();
      //  JOptionPane.showMessageDialog(null, quantidade);
        if (status == false) {

            List<Produto> p = contProd.nomeIgual(txtNomeProduto.getText());

            if (p.isEmpty()) {
                //  System.out.println("OIEEEE");
                if (quantidade <= 0) {
                    JOptionPane.showMessageDialog(null, "Digite uma Quantidade Minima SUPERIOR A 0");
                } else {
                    if (!txtNomeProduto.getText().equals("") || !txtValorVenda.getText().equals("")) {
                        produto.setProdutoNome(txtNomeProduto.getText());
                        produto.setProdutoQuantidade((0));

                        String valor = txtValorVenda.getText();
                        String comVirgula = valor.replace(',', '.');

                        try {
                            produto.setQuantidadeMinima(quantidade);
                            produto.setProdutoValorVenda(Float.parseFloat(comVirgula));
                            contProd.gravarNovo(produto);
                            dispose();
                        } catch (NumberFormatException x) {
                            JOptionPane.showMessageDialog(null, "POR FAVOR PREENCHER O PREÇO DO PRODUTO COM NUMERO");
                        }

                    } else {
                        JOptionPane.showMessageDialog(null, "PREENCHA TODOS OS CAMPOS!!!");
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "PRODUTO JÁ CADASTRADO");
            }
        } else {
            if (quantidade <= 0) {
                JOptionPane.showMessageDialog(null, "Digite uma Quantidade Minima Superior a 0");
            } else {
                if (!txtNomeProduto.getText().equals("") || !txtValorVenda.getText().equals("")) {
                    if (produto.getProdutoNome().equals(txtNomeProduto.getText())) {
                        produto.setProdutoNome(txtNomeProduto.getText());
                        produto.setQuantidadeMinima(jSpinQuantMinima.getValue());
                        String valor = txtValorVenda.getText();
                        String comVirgula = valor.replace(',', '.');
                        try {
                            produto.setProdutoValorVenda(Float.parseFloat(comVirgula));
                            contProd.salvarAlteracao(produto);
                            dispose();
                        } catch (NumberFormatException x) {
                            JOptionPane.showMessageDialog(null, "POR FAVOR PREENCHER O PREÇO DO PRODUTO COM NUMERO");
                        }
                    } else {
                        List<Produto> p = contProd.nomeIgual(txtNomeProduto.getText());
                        if (p.isEmpty()) {
                            produto.setProdutoNome(txtNomeProduto.getText());
                            produto.setQuantidadeMinima(quantidade);
                            String valor = txtValorVenda.getText();
                            String comVirgula = valor.replace(',', '.');
                            try {
                                produto.setProdutoValorVenda(Float.parseFloat(comVirgula));
                                contProd.salvarAlteracao(produto);
                                dispose();
                            } catch (NumberFormatException x) {
                                JOptionPane.showMessageDialog(null, "POR FAVOR PREENCHER O PREÇO DO PRODUTO COM NUMERO");
                            }

                        } else {
                            JOptionPane.showMessageDialog(null, "PRODUTO JÁ CADASTRADO");
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "PREENCHA TODOS OS CAMPOS!!!");
                }
            }
        }


    }//GEN-LAST:event_botSalvarActionPerformed


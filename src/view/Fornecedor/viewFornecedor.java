    private void botAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botAlterarActionPerformed

        int linha = -1;
        linha = tabFornecedor.getSelectedRow();
        if (linha != -1) {
            boolean consulta = true;
            fornecedor = listFornecedor.get(linha);
            viewDadosFornecedor alterar = new viewDadosFornecedor(null, true, fornecedor, consulta);
            alterar.setVisible(true);
            listFornecedor.clear();
            listFornecedor.addAll(contFornecedor.listarTodos());
            limparLab();
            fornecedor = null;
        } else {
            JOptionPane.showMessageDialog(null, "SELECIONE ALGUÉM NA TABELA PARA ALTERAR");
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_botAlterarActionPerformed

    private void botExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botExcluirActionPerformed

        int linhaExcluir = -1;
        linhaExcluir = tabFornecedor.getSelectedRow();
        if (linhaExcluir != -1) {
            fornecedor = listFornecedor.get(linhaExcluir);
            int op = JOptionPane.showConfirmDialog(this,
                    "Confirma a exclusão do fornecedor "
                    + fornecedor.getForncedorNomeContato() + "da empresa " + fornecedor.getFornecedorNomeFantasia() + "?",
                    "Exclusão de fornecedor",
                    JOptionPane.YES_NO_OPTION);

            if (op == JOptionPane.YES_OPTION) {
                // chama o controle para excluir o usuário
                try {
                    contFornecedor.excluir(fornecedor.getForncedorID());
                    /// JOptionPane.showMessageDialog(this, "O usuário foi excluído.");
                    listFornecedor.clear();
                    listFornecedor.addAll(contFornecedor.listarTodos());
                    limparLab();
                } catch (NaoExisteException | IntegridadeException e) {

                    JOptionPane.showMessageDialog(this, e.getMessage());
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Funcionário Na Tabela Para Ser Excluido");
        }
    }//GEN-LAST:event_botExcluirActionPerformed


    private void tabFornecedorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabFornecedorMouseClicked
        int linha = -1;
        linha = tabFornecedor.getSelectedRow();

        if (evt.getClickCount() > 1) {
            boolean consulta = true;
            viewDadosFornecedor insAlte = new viewDadosFornecedor(null, true, fornecedor, consulta);
            insAlte.setVisible(true);
            limparLab();
            listFornecedor.clear();
            listFornecedor.addAll(contFornecedor.listarTodos());
        } else {
            if (linha != -1) {
                fornecedor = new Fornecedor();
                fornecedor = listFornecedor.get(linha);
                labRazaoSocial.setText(fornecedor.getFornecedorRazaoSicial());
                labCidade.setText(fornecedor.getFornecedorCidade() + "  Estado:  " + fornecedor.getFornecedorEstado());
                labNomeContato.setText(fornecedor.getForncedorNomeContato());

                labCNPJ.setText(fornecedor.getFornecedorCNPJ());
                labNomeFantasia.setText(fornecedor.getFornecedorNomeFantasia());
                labEmail.setText(fornecedor.getFornecedorEmail());
                // labEstado.setText(fornecedor.getFornecedorEstado());

                labTelefone.setText(fornecedor.getFornecedorTelefoneCelular());
            }
        }
    }//GEN-LAST:event_tabFornecedorMouseClicked

